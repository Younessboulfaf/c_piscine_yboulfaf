int	ft_atoi(char *str)
{
	int	r;
	int	s;
	int	i;

	r = 0;
	s = 1;
	i = 0;
	while (str[i] && (str[i] >= 0 && str[i] <= 32))
		i++;
	while (str[i] && (str[i] == '-' || str[i] == '+' ))
	{
		if (str[i] == '-')
			s = s * (-1);
		i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		r = r * 10 + (str[i] - '0');
		i++;
	}
	return (s * r);
}
